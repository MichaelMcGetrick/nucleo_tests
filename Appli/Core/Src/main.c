// Timestamp string for logging with printf:
char * timestamp()
{

	unsigned int tick, rem, num, fac;
	unsigned int ts[4];

	// Get msec tick from system:
	tick = HAL_GetTick();
	num = tick;

	for (int i=3; i > 0; i--)
	{
		fac = (i == 3) ? 1000 : 60;
		rem = num % fac;
		num = num / fac;
		ts[i] = rem;
		ts[i-1] = num;


	}

	sprintf(time_str,"%02d:%02d:%02d.%03d ",ts[0],ts[1],ts[2],ts[3]);

	return time_str;


}
